# Generate the tag_model_mapping dictionary
tag_model_mapping = {}

for tag in opc_tags:
    # print(f"Processing tag: {tag}")
    prefix, suffix = None, None
    
    # Try to match the prefix from the known prefixes
    for known_prefix in prefix_model_mapping:
        if tag.startswith(known_prefix):
            prefix = known_prefix
            suffix = tag[len(known_prefix) + 1:]  # +1 to account for the underscore
            break

    # print(f"Extracted prefix: {prefix}")
    # print(f"Extracted suffix: {suffix}")

    # Get the corresponding model and field names
    model_name = prefix_model_mapping.get(prefix)
    field_name = suffix_field_mapping.get(suffix)

    # print(f"Model name found: {model_name}")
    # print(f"Field name found: {field_name}")

    if model_name and field_name:
        tag_model_mapping[tag] = (model_name, field_name)

